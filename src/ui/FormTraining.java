    private void jTblTrainsetMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTblTrainsetMouseClicked
        // TODO add your handling code here:
      int baris = jTblTrainset.rowAtPoint(evt.getPoint());    
      FingerPrintImage fpimg = ts.getFingerPrintImage(baris);
      FPImageViewer fpiv = new FPImageViewer();
      fpiv.setImage(fpimg);
      fpiv.setViewer(jlblViewImage);
      fpiv.viewImageDefault();
      
      Preprocessor prep = new Preprocessor(fpimg.getHeight(), fpimg.getWidth());
      fpimg = prep.binarize(fpimg);
           
      FeatureExtractor fe = new  FeatureExtractor();
      Input inp =fe.extract(fpimg);
      
      jlblRTVR.setText(String.format("%.4f", inp.getValue(0)));
      jlblKepadatanRidge.setText(String.format("%.4f", inp.getValue(2)));
     
      fpiv.viewImageOutput();
        
    }//GEN-LAST:event_jTblTrainsetMouseClicked

