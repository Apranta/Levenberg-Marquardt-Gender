    private void jbtnLatihJaringanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnLatihJaringanActionPerformed
        try {
            // TODO add your handling code here:
            
            NetworkSetting ns = new NetworkSetting();
            ns.setErrTolerance(Double.parseDouble(jTxtErrTolerance.getText()));
            ns.setNNeuronOutput(1);
            ns.setNNeuronHidden(Integer.parseInt(jTxtMaxHidden.getText()));
            ns.setLearningRate(Double.parseDouble(jTxtLearningRate.getText()));
            ns.setMaxEpoch(Integer.parseInt(jTxtMaxEpoch.getText()));            
            System.out.println("Training Siap Dimulai...");
            
            NetworkWriter.writeSetting("setting", "network", ns);
            
            if (jRbtnLevenMar.isSelected())
            {
                LevenbergMarquardtTrainer lmt = new LevenbergMarquardtTrainer(ts, this, ns);
                lmt.train();
            }else if (jRbtnBackPro.isSelected())
            {
                BackPropagationTrainer jsttrainer = new  BackPropagationTrainer(ts, this,ns);
                jsttrainer.train();
            }
        } catch (IOException ex) {
            Logger.getLogger(FormTraining.class.getName()).log(Level.SEVERE, null, ex);
        }
       

         
    }//GEN-LAST:event_jbtnLatihJaringanActionPerformed

