    private void showFilesImages()
    {
        String[] header = new String[]{"No","Image FileName","Target"};
        String[][] data = new String[ts.getTotalFingerPrint()][header.length];
        for(int i=0;i<ts.getTotalFingerPrint();i++)
        {     
                String ffname = ts.getFingerPrintImage(i).getName();
                // System.out.println("FFName " + ffname);
                data[i][0] = String.valueOf(i+1);
                data[i][1] = ts.getFileNames(i);
                data[i][2] = ffname;
         }
            jTblTrainset.setModel(new DefaultTableModel(data, header));

    }
