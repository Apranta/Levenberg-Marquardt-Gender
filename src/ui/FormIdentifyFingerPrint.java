private void jbtnKenaliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnKenaliActionPerformed
// TODO add your handling code here: 
     int typeofweights = -1;
     
     if (jRbtnLM.isSelected())
     {
         typeofweights = FingerPrintIdentifier.LEVENBERGMARQUARDT_WEIGHTS;
     }else if (jRbtnBP.isSelected())
     {
         typeofweights = FingerPrintIdentifier.BACKPROPAGATION_WEIGHTS;
     }
     
     for(int i=0;i<ujisamples.getTotalFingerPrint();i++) 
     {
        FingerPrintImage fpimg = ujisamples.getFingerPrintImage(i);
        FingerPrintIdentifier fpi= new FingerPrintIdentifier(fpimg,typeofweights );
        String result = fpi.GetID();
        jlblHasil.setText(result);
        ujisamples.getFingerPrintImage(i).SetName(result);
     }
     showFilesImages();
   
}//GEN-LAST:event_jbtnKenaliActionPerformed

