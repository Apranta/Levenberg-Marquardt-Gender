    private void showFilesImages()
    {
      
        String[] header = new String[]{"No","Image FileName","Result"};
        String[][] data = new String[ujisamples.getTotalFingerPrint()][header.length];
        for(int i=0;i<ujisamples.getTotalFingerPrint();i++)
        {     
            String ffname = ujisamples.getFingerPrintImage(i).getName();
            // System.out.println("FFName " + ffname);
            data[i][0] = String.valueOf(i+1);
            data[i][1] = ujisamples.getFileNames(i);
          
              data[i][2] = ffname;
           
        
        }
        jtblDataUji.setModel(new DefaultTableModel(data, header));
}
    
